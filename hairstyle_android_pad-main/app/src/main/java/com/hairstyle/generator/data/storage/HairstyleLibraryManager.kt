    /**
     * 根据分类ID获取发型列表
     */
    fun getHairstylesByCategory(categoryId: String): List<HairstyleTemplate> {
        val allCategories = getFemaleCategories() + getMaleCategories()
        return allCategories.find { it.id == categoryId }?.hairstyles ?: emptyList()
    }

    /**
     * 搜索发型
     */
    fun searchHairstyles(query: String): List<HairstyleTemplate> {
        val allCategories = getFemaleCategories() + getMaleCategories()
        val allHairstyles = allCategories.flatMap { it.hairstyles }

        return allHairstyles.filter { hairstyle ->
            hairstyle.name.contains(query, ignoreCase = true) ||
            hairstyle.description.contains(query, ignoreCase = true) ||
            hairstyle.category.contains(query, ignoreCase = true)
        }
    }
