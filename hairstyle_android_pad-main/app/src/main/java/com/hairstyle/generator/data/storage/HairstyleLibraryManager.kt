    /**
     * 解析发型文件名
     */
    private fun parseHairstyleName(fileName: String): Triple<String, String, String>? {
        val nameWithoutExtension = fileName.substringBeforeLast(".")
        val parts = nameWithoutExtension.split("_")

        return if (parts.size >= 3) {
            val category = parts[0]
            val name = parts[1]
            val description = parts.drop(2).joinToString("_")
            Triple(category, name, description)
        } else {
            null
        }
    }

    /**
     * 获取所有女性发型分类
     */
    fun getFemaleCategories(): List<HairstyleCategory> {
        val categories = mutableMapOf<String, MutableList<HairstyleTemplate>>()

        FEMALE_HAIRSTYLES.forEachIndexed { index, fileName ->
            parseHairstyleName(fileName)?.let { (category, name, description) ->
                val hairstyle = HairstyleTemplate(
                    id = "female_${index}",
                    name = name,
                    description = description,
                    imageUrl = HairstyleImageConfig.buildImagePath(category, name, description, "女"),
                    category = category,
                    gender = "女"
                )

                if (!categories.containsKey(category)) {
                    categories[category] = mutableListOf()
                }
                categories[category]?.add(hairstyle)
            }
        }

        return categories.map { (categoryName, hairstyles) ->
            HairstyleCategory(
                id = "female_$categoryName",
                name = categoryName,
                gender = "女",
                hairstyles = hairstyles.toList()
            )
        }.sortedBy { it.name }
    }

